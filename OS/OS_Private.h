#ifndef OS_PRIVATE_H_
#define OS_PRIVATE_H_

/* Define the hexadecimal values for each digit (0-9) */
#define ZERO 0x3F    /* 0x3F represents the segments to display digit '0' on a 7-segment display */
#define ONE 0x06     /* 0x06 represents the segments to display digit '1' */
#define TWO 0x5B     /* 0x5B represents the segments to display digit '2' */
#define THREE 0x4F   /* 0x4F represents the segments to display digit '3' */
#define FOUR 0x66    /* 0x66 represents the segments to display digit '4' */
#define FIVE 0x6D    /* 0x6D represents the segments to display digit '5' */
#define SIX 0x7D     /* 0x7D represents the segments to display digit '6' */
#define SEVEN 0x07   /* 0x07 represents the segments to display digit '7' */
#define EIGHT 0x7F   /* 0x7F represents the segments to display digit '8' */
#define NINE 0x6F    /* 0x6F represents the segments to display digit '9' */

#endif /* OS_PRIVATE_H_ */
